public class switchcase {
    public static void main(String[] args) {
        int number = 4;
        switch (number) {
            case 1:
                System.out.println("apple");
                break;
            case 2:
                System.out.println("bannana");
                break;
            case 3:
                System.out.println("mango & bannana");
                break;
            default:
                System.out.println("wake up");
        }
    }
}
